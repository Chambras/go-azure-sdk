var _ resourceids.ResourceId = ConfigId{}

func TestNewConfigID(t *testing.T) {
	id := NewConfigID("12345678-1234-9876-4563-123456789012", "example-resource-group", "namespaceValue", "$default")

	if id.SubscriptionId != "12345678-1234-9876-4563-123456789012" {
		t.Fatalf("Expected %q but got %q for Segment 'SubscriptionId'", id.SubscriptionId, "12345678-1234-9876-4563-123456789012")
	}

	if id.ResourceGroupName != "example-resource-group" {
		t.Fatalf("Expected %q but got %q for Segment 'ResourceGroupName'", id.ResourceGroupName, "example-resource-group")
	}

	if id.NamespaceName != "namespaceValue" {
		t.Fatalf("Expected %q but got %q for Segment 'NamespaceName'", id.NamespaceName, "namespaceValue")
	}

	if id.ConfigName != "$default" {
		t.Fatalf("Expected %q but got %q for Segment 'ConfigName'", id.ConfigName, "$default")
	}
}

func TestFormatConfigID(t *testing.T) {
	actual := NewConfigID("12345678-1234-9876-4563-123456789012", "example-resource-group", "namespaceValue", "$default").ID()
	expected := "/subscriptions/12345678-1234-9876-4563-123456789012/resourceGroups/example-resource-group/providers/Microsoft.ServiceBus/namespaces/namespaceValue/migrationConfigurations/$default"
	if actual != expected {
		t.Fatalf("Expected the Formatted ID to be %q but got %q", expected, actual)
	}
}

