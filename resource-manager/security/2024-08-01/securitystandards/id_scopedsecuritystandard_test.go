var _ resourceids.ResourceId = &ScopedSecurityStandardId{}

func TestNewScopedSecurityStandardID(t *testing.T) {
	id := NewScopedSecurityStandardID("/subscriptions/12345678-1234-9876-4563-123456789012/resourceGroups/some-resource-group", "standardId")

	if id.Scope != "/subscriptions/12345678-1234-9876-4563-123456789012/resourceGroups/some-resource-group" {
		t.Fatalf("Expected %q but got %q for Segment 'Scope'", id.Scope, "/subscriptions/12345678-1234-9876-4563-123456789012/resourceGroups/some-resource-group")
	}

	if id.StandardId != "standardId" {
		t.Fatalf("Expected %q but got %q for Segment 'StandardId'", id.StandardId, "standardId")
	}
}

