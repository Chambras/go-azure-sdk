var _ resourceids.ResourceId = &SiteCertificateId{}

func TestNewSiteCertificateID(t *testing.T) {
	id := NewSiteCertificateID("12345678-1234-9876-4563-123456789012", "example-resource-group", "siteName", "certificateName")

	if id.SubscriptionId != "12345678-1234-9876-4563-123456789012" {
		t.Fatalf("Expected %q but got %q for Segment 'SubscriptionId'", id.SubscriptionId, "12345678-1234-9876-4563-123456789012")
	}

	if id.ResourceGroupName != "example-resource-group" {
		t.Fatalf("Expected %q but got %q for Segment 'ResourceGroupName'", id.ResourceGroupName, "example-resource-group")
	}

	if id.SiteName != "siteName" {
		t.Fatalf("Expected %q but got %q for Segment 'SiteName'", id.SiteName, "siteName")
	}

	if id.CertificateName != "certificateName" {
		t.Fatalf("Expected %q but got %q for Segment 'CertificateName'", id.CertificateName, "certificateName")
	}
}

func TestFormatSiteCertificateID(t *testing.T) {
	actual := NewSiteCertificateID("12345678-1234-9876-4563-123456789012", "example-resource-group", "siteName", "certificateName").ID()
	expected := "/subscriptions/12345678-1234-9876-4563-123456789012/resourceGroups/example-resource-group/providers/Microsoft.Web/sites/siteName/certificates/certificateName"
	if actual != expected {
		t.Fatalf("Expected the Formatted ID to be %q but got %q", expected, actual)
	}
}

