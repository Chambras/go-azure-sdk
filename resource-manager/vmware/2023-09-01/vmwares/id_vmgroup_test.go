var _ resourceids.ResourceId = &VMGroupId{}

func TestNewVMGroupID(t *testing.T) {
	id := NewVMGroupID("12345678-1234-9876-4563-123456789012", "example-resource-group", "privateCloudName", "vmGroupId")

	if id.SubscriptionId != "12345678-1234-9876-4563-123456789012" {
		t.Fatalf("Expected %q but got %q for Segment 'SubscriptionId'", id.SubscriptionId, "12345678-1234-9876-4563-123456789012")
	}

	if id.ResourceGroupName != "example-resource-group" {
		t.Fatalf("Expected %q but got %q for Segment 'ResourceGroupName'", id.ResourceGroupName, "example-resource-group")
	}

	if id.PrivateCloudName != "privateCloudName" {
		t.Fatalf("Expected %q but got %q for Segment 'PrivateCloudName'", id.PrivateCloudName, "privateCloudName")
	}

	if id.VmGroupId != "vmGroupId" {
		t.Fatalf("Expected %q but got %q for Segment 'VmGroupId'", id.VmGroupId, "vmGroupId")
	}
}

func TestFormatVMGroupID(t *testing.T) {
	actual := NewVMGroupID("12345678-1234-9876-4563-123456789012", "example-resource-group", "privateCloudName", "vmGroupId").ID()
	expected := "/subscriptions/12345678-1234-9876-4563-123456789012/resourceGroups/example-resource-group/providers/Microsoft.AVS/privateClouds/privateCloudName/workloadNetworks/default/vmGroups/vmGroupId"
	if actual != expected {
		t.Fatalf("Expected the Formatted ID to be %q but got %q", expected, actual)
	}
}

