func TestNewDnsResolverDomainListID(t *testing.T) {
	id := NewDnsResolverDomainListID("12345678-1234-9876-4563-123456789012", "example-resource-group", "dnsResolverDomainListName")

	if id.SubscriptionId != "12345678-1234-9876-4563-123456789012" {
		t.Fatalf("Expected %q but got %q for Segment 'SubscriptionId'", id.SubscriptionId, "12345678-1234-9876-4563-123456789012")
	}

	if id.ResourceGroupName != "example-resource-group" {
		t.Fatalf("Expected %q but got %q for Segment 'ResourceGroupName'", id.ResourceGroupName, "example-resource-group")
	}

	if id.DnsResolverDomainListName != "dnsResolverDomainListName" {
		t.Fatalf("Expected %q but got %q for Segment 'DnsResolverDomainListName'", id.DnsResolverDomainListName, "dnsResolverDomainListName")
	}
}

