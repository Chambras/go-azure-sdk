type ApplicationType string

const (
	ApplicationTypeCDI  ApplicationType = "CDI"
	ApplicationTypeCDIE ApplicationType = "CDIE"
)

func PossibleValuesForApplicationType() []string {
	return []string{
		string(ApplicationTypeCDI),
		string(ApplicationTypeCDIE),
	}
}

func (s *ApplicationType) UnmarshalJSON(bytes []byte) error {
	var decoded string
	if err := json.Unmarshal(bytes, &decoded); err != nil {
		return fmt.Errorf("unmarshaling: %+v", err)
	}
	out, err := parseApplicationType(decoded)
	if err != nil {
		return fmt.Errorf("parsing %q: %+v", decoded, err)
	}
	*s = *out
	return nil
}

func parseApplicationType(input string) (*ApplicationType, error) {
	vals := map[string]ApplicationType{
		"cdi":  ApplicationTypeCDI,
		"cdie": ApplicationTypeCDIE,
	}
	if v, ok := vals[strings.ToLower(input)]; ok {
		return &v, nil
	}

	// otherwise presume it's an undefined value and best-effort it
	out := ApplicationType(input)
	return &out, nil
}

