var _ resourceids.ResourceId = &RecommendedActionId{}

func TestNewRecommendedActionID(t *testing.T) {
	id := NewRecommendedActionID("12345678-1234-9876-4563-123456789012", "example-resource-group", "serverName", "advisorName", "recommendedActionName")

	if id.SubscriptionId != "12345678-1234-9876-4563-123456789012" {
		t.Fatalf("Expected %q but got %q for Segment 'SubscriptionId'", id.SubscriptionId, "12345678-1234-9876-4563-123456789012")
	}

	if id.ResourceGroupName != "example-resource-group" {
		t.Fatalf("Expected %q but got %q for Segment 'ResourceGroupName'", id.ResourceGroupName, "example-resource-group")
	}

	if id.ServerName != "serverName" {
		t.Fatalf("Expected %q but got %q for Segment 'ServerName'", id.ServerName, "serverName")
	}

	if id.AdvisorName != "advisorName" {
		t.Fatalf("Expected %q but got %q for Segment 'AdvisorName'", id.AdvisorName, "advisorName")
	}

	if id.RecommendedActionName != "recommendedActionName" {
		t.Fatalf("Expected %q but got %q for Segment 'RecommendedActionName'", id.RecommendedActionName, "recommendedActionName")
	}
}

func TestFormatRecommendedActionID(t *testing.T) {
	actual := NewRecommendedActionID("12345678-1234-9876-4563-123456789012", "example-resource-group", "serverName", "advisorName", "recommendedActionName").ID()
	expected := "/subscriptions/12345678-1234-9876-4563-123456789012/resourceGroups/example-resource-group/providers/Microsoft.DBforMariaDB/servers/serverName/advisors/advisorName/recommendedActions/recommendedActionName"
	if actual != expected {
		t.Fatalf("Expected the Formatted ID to be %q but got %q", expected, actual)
	}
}

