func TestSegmentsForSummaryLogId(t *testing.T) {
	segments := SummaryLogId{}.Segments()
	if len(segments) == 0 {
		t.Fatalf("SummaryLogId has no segments")
	}

	uniqueNames := make(map[string]struct{}, 0)
	for _, segment := range segments {
		uniqueNames[segment.Name] = struct{}{}
	}
	if len(uniqueNames) != len(segments) {
		t.Fatalf("Expected the Segments to be unique but got %d unique segments and %d total segments", len(uniqueNames), len(segments))
	}
}
