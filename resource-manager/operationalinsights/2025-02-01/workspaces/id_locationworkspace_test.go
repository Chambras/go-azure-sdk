var _ resourceids.ResourceId = &LocationWorkspaceId{}

func TestNewLocationWorkspaceID(t *testing.T) {
	id := NewLocationWorkspaceID("12345678-1234-9876-4563-123456789012", "example-resource-group", "locationName", "workspaceName")

	if id.SubscriptionId != "12345678-1234-9876-4563-123456789012" {
		t.Fatalf("Expected %q but got %q for Segment 'SubscriptionId'", id.SubscriptionId, "12345678-1234-9876-4563-123456789012")
	}

	if id.ResourceGroupName != "example-resource-group" {
		t.Fatalf("Expected %q but got %q for Segment 'ResourceGroupName'", id.ResourceGroupName, "example-resource-group")
	}

	if id.LocationName != "locationName" {
		t.Fatalf("Expected %q but got %q for Segment 'LocationName'", id.LocationName, "locationName")
	}

	if id.WorkspaceName != "workspaceName" {
		t.Fatalf("Expected %q but got %q for Segment 'WorkspaceName'", id.WorkspaceName, "workspaceName")
	}
}

func TestFormatLocationWorkspaceID(t *testing.T) {
	actual := NewLocationWorkspaceID("12345678-1234-9876-4563-123456789012", "example-resource-group", "locationName", "workspaceName").ID()
	expected := "/subscriptions/12345678-1234-9876-4563-123456789012/resourceGroups/example-resource-group/providers/Microsoft.OperationalInsights/locations/locationName/workspaces/workspaceName"
	if actual != expected {
		t.Fatalf("Expected the Formatted ID to be %q but got %q", expected, actual)
	}
}

