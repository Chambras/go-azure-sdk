var _ resourceids.ResourceId = TableServiceId{}

func TestNewTableServiceID(t *testing.T) {
	id := NewTableServiceID("12345678-1234-9876-4563-123456789012", "example-resource-group", "accountValue", "default")

	if id.SubscriptionId != "12345678-1234-9876-4563-123456789012" {
		t.Fatalf("Expected %q but got %q for Segment 'SubscriptionId'", id.SubscriptionId, "12345678-1234-9876-4563-123456789012")
	}

	if id.ResourceGroupName != "example-resource-group" {
		t.Fatalf("Expected %q but got %q for Segment 'ResourceGroupName'", id.ResourceGroupName, "example-resource-group")
	}

	if id.AccountName != "accountValue" {
		t.Fatalf("Expected %q but got %q for Segment 'AccountName'", id.AccountName, "accountValue")
	}

	if id.TableServiceName != "default" {
		t.Fatalf("Expected %q but got %q for Segment 'TableServiceName'", id.TableServiceName, "default")
	}
}

func TestFormatTableServiceID(t *testing.T) {
	actual := NewTableServiceID("12345678-1234-9876-4563-123456789012", "example-resource-group", "accountValue", "default").ID()
	expected := "/subscriptions/12345678-1234-9876-4563-123456789012/resourceGroups/example-resource-group/providers/Microsoft.Storage/storageAccounts/accountValue/tableServices/default"
	if actual != expected {
		t.Fatalf("Expected the Formatted ID to be %q but got %q", expected, actual)
	}
}

