var _ resourceids.ResourceId = QueueServiceId{}

func TestNewQueueServiceID(t *testing.T) {
	id := NewQueueServiceID("12345678-1234-9876-4563-123456789012", "example-resource-group", "accountValue", "default")

	if id.SubscriptionId != "12345678-1234-9876-4563-123456789012" {
		t.Fatalf("Expected %q but got %q for Segment 'SubscriptionId'", id.SubscriptionId, "12345678-1234-9876-4563-123456789012")
	}

	if id.ResourceGroupName != "example-resource-group" {
		t.Fatalf("Expected %q but got %q for Segment 'ResourceGroupName'", id.ResourceGroupName, "example-resource-group")
	}

	if id.AccountName != "accountValue" {
		t.Fatalf("Expected %q but got %q for Segment 'AccountName'", id.AccountName, "accountValue")
	}

	if id.QueueServiceName != "default" {
		t.Fatalf("Expected %q but got %q for Segment 'QueueServiceName'", id.QueueServiceName, "default")
	}
}

func TestFormatQueueServiceID(t *testing.T) {
	actual := NewQueueServiceID("12345678-1234-9876-4563-123456789012", "example-resource-group", "accountValue", "default").ID()
	expected := "/subscriptions/12345678-1234-9876-4563-123456789012/resourceGroups/example-resource-group/providers/Microsoft.Storage/storageAccounts/accountValue/queueServices/default"
	if actual != expected {
		t.Fatalf("Expected the Formatted ID to be %q but got %q", expected, actual)
	}
}

