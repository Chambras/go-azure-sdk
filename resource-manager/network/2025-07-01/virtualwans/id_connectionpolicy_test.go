func TestNewConnectionPolicyID(t *testing.T) {
	id := NewConnectionPolicyID("12345678-1234-9876-4563-123456789012", "example-resource-group", "virtualHubName", "connectionPolicyName")

	if id.SubscriptionId != "12345678-1234-9876-4563-123456789012" {
		t.Fatalf("Expected %q but got %q for Segment 'SubscriptionId'", id.SubscriptionId, "12345678-1234-9876-4563-123456789012")
	}

	if id.ResourceGroupName != "example-resource-group" {
		t.Fatalf("Expected %q but got %q for Segment 'ResourceGroupName'", id.ResourceGroupName, "example-resource-group")
	}

	if id.VirtualHubName != "virtualHubName" {
		t.Fatalf("Expected %q but got %q for Segment 'VirtualHubName'", id.VirtualHubName, "virtualHubName")
	}

	if id.ConnectionPolicyName != "connectionPolicyName" {
		t.Fatalf("Expected %q but got %q for Segment 'ConnectionPolicyName'", id.ConnectionPolicyName, "connectionPolicyName")
	}
}

