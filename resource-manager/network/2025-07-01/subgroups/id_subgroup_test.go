func TestNewSubgroupID(t *testing.T) {
	id := NewSubgroupID("12345678-1234-9876-4563-123456789012", "example-resource-group", "interconnectGroupName", "subgroupName")

	if id.SubscriptionId != "12345678-1234-9876-4563-123456789012" {
		t.Fatalf("Expected %q but got %q for Segment 'SubscriptionId'", id.SubscriptionId, "12345678-1234-9876-4563-123456789012")
	}

	if id.ResourceGroupName != "example-resource-group" {
		t.Fatalf("Expected %q but got %q for Segment 'ResourceGroupName'", id.ResourceGroupName, "example-resource-group")
	}

	if id.InterconnectGroupName != "interconnectGroupName" {
		t.Fatalf("Expected %q but got %q for Segment 'InterconnectGroupName'", id.InterconnectGroupName, "interconnectGroupName")
	}

	if id.SubgroupName != "subgroupName" {
		t.Fatalf("Expected %q but got %q for Segment 'SubgroupName'", id.SubgroupName, "subgroupName")
	}
}

func TestFormatSubgroupID(t *testing.T) {
	actual := NewSubgroupID("12345678-1234-9876-4563-123456789012", "example-resource-group", "interconnectGroupName", "subgroupName").ID()
	expected := "/subscriptions/12345678-1234-9876-4563-123456789012/resourceGroups/example-resource-group/providers/Microsoft.Network/interconnectGroups/interconnectGroupName/subgroups/subgroupName"
	if actual != expected {
		t.Fatalf("Expected the Formatted ID to be %q but got %q", expected, actual)
	}
}

