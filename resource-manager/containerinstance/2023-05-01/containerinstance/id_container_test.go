var _ resourceids.ResourceId = &ContainerId{}

func TestNewContainerID(t *testing.T) {
	id := NewContainerID("12345678-1234-9876-4563-123456789012", "example-resource-group", "containerGroupName", "containerName")

	if id.SubscriptionId != "12345678-1234-9876-4563-123456789012" {
		t.Fatalf("Expected %q but got %q for Segment 'SubscriptionId'", id.SubscriptionId, "12345678-1234-9876-4563-123456789012")
	}

	if id.ResourceGroupName != "example-resource-group" {
		t.Fatalf("Expected %q but got %q for Segment 'ResourceGroupName'", id.ResourceGroupName, "example-resource-group")
	}

	if id.ContainerGroupName != "containerGroupName" {
		t.Fatalf("Expected %q but got %q for Segment 'ContainerGroupName'", id.ContainerGroupName, "containerGroupName")
	}

	if id.ContainerName != "containerName" {
		t.Fatalf("Expected %q but got %q for Segment 'ContainerName'", id.ContainerName, "containerName")
	}
}

func TestFormatContainerID(t *testing.T) {
	actual := NewContainerID("12345678-1234-9876-4563-123456789012", "example-resource-group", "containerGroupName", "containerName").ID()
	expected := "/subscriptions/12345678-1234-9876-4563-123456789012/resourceGroups/example-resource-group/providers/Microsoft.ContainerInstance/containerGroups/containerGroupName/containers/containerName"
	if actual != expected {
		t.Fatalf("Expected the Formatted ID to be %q but got %q", expected, actual)
	}
}

