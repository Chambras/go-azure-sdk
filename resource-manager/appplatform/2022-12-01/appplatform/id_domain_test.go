var _ resourceids.ResourceId = DomainId{}

func TestNewDomainID(t *testing.T) {
	id := NewDomainID("12345678-1234-9876-4563-123456789012", "example-resource-group", "serviceValue", "appValue", "domainValue")

	if id.SubscriptionId != "12345678-1234-9876-4563-123456789012" {
		t.Fatalf("Expected %q but got %q for Segment 'SubscriptionId'", id.SubscriptionId, "12345678-1234-9876-4563-123456789012")
	}

	if id.ResourceGroupName != "example-resource-group" {
		t.Fatalf("Expected %q but got %q for Segment 'ResourceGroupName'", id.ResourceGroupName, "example-resource-group")
	}

	if id.ServiceName != "serviceValue" {
		t.Fatalf("Expected %q but got %q for Segment 'ServiceName'", id.ServiceName, "serviceValue")
	}

	if id.AppName != "appValue" {
		t.Fatalf("Expected %q but got %q for Segment 'AppName'", id.AppName, "appValue")
	}

	if id.DomainName != "domainValue" {
		t.Fatalf("Expected %q but got %q for Segment 'DomainName'", id.DomainName, "domainValue")
	}
}

func TestFormatDomainID(t *testing.T) {
	actual := NewDomainID("12345678-1234-9876-4563-123456789012", "example-resource-group", "serviceValue", "appValue", "domainValue").ID()
	expected := "/subscriptions/12345678-1234-9876-4563-123456789012/resourceGroups/example-resource-group/providers/Microsoft.AppPlatform/spring/serviceValue/apps/appValue/domains/domainValue"
	if actual != expected {
		t.Fatalf("Expected the Formatted ID to be %q but got %q", expected, actual)
	}
}

