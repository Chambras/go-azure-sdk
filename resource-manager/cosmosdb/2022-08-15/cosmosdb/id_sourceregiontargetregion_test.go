func TestNewSourceRegionTargetRegionID(t *testing.T) {
	id := NewSourceRegionTargetRegionID("12345678-1234-9876-4563-123456789012", "example-resource-group", "accountValue", "sourceRegionValue", "targetRegionValue")

	if id.SubscriptionId != "12345678-1234-9876-4563-123456789012" {
		t.Fatalf("Expected %q but got %q for Segment 'SubscriptionId'", id.SubscriptionId, "12345678-1234-9876-4563-123456789012")
	}

	if id.ResourceGroupName != "example-resource-group" {
		t.Fatalf("Expected %q but got %q for Segment 'ResourceGroupName'", id.ResourceGroupName, "example-resource-group")
	}

	if id.AccountName != "accountValue" {
		t.Fatalf("Expected %q but got %q for Segment 'AccountName'", id.AccountName, "accountValue")
	}

	if id.SourceRegion != "sourceRegionValue" {
		t.Fatalf("Expected %q but got %q for Segment 'SourceRegion'", id.SourceRegion, "sourceRegionValue")
	}

	if id.TargetRegion != "targetRegionValue" {
		t.Fatalf("Expected %q but got %q for Segment 'TargetRegion'", id.TargetRegion, "targetRegionValue")
	}
}

