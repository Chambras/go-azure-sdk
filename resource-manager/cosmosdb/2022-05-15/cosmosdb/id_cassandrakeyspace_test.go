var _ resourceids.ResourceId = CassandraKeyspaceId{}

func TestNewCassandraKeyspaceID(t *testing.T) {
	id := NewCassandraKeyspaceID("12345678-1234-9876-4563-123456789012", "example-resource-group", "accountValue", "keyspaceValue")

	if id.SubscriptionId != "12345678-1234-9876-4563-123456789012" {
		t.Fatalf("Expected %q but got %q for Segment 'SubscriptionId'", id.SubscriptionId, "12345678-1234-9876-4563-123456789012")
	}

	if id.ResourceGroupName != "example-resource-group" {
		t.Fatalf("Expected %q but got %q for Segment 'ResourceGroupName'", id.ResourceGroupName, "example-resource-group")
	}

	if id.AccountName != "accountValue" {
		t.Fatalf("Expected %q but got %q for Segment 'AccountName'", id.AccountName, "accountValue")
	}

	if id.KeyspaceName != "keyspaceValue" {
		t.Fatalf("Expected %q but got %q for Segment 'KeyspaceName'", id.KeyspaceName, "keyspaceValue")
	}
}

func TestFormatCassandraKeyspaceID(t *testing.T) {
	actual := NewCassandraKeyspaceID("12345678-1234-9876-4563-123456789012", "example-resource-group", "accountValue", "keyspaceValue").ID()
	expected := "/subscriptions/12345678-1234-9876-4563-123456789012/resourceGroups/example-resource-group/providers/Microsoft.DocumentDB/databaseAccounts/accountValue/cassandraKeyspaces/keyspaceValue"
	if actual != expected {
		t.Fatalf("Expected the Formatted ID to be %q but got %q", expected, actual)
	}
}

