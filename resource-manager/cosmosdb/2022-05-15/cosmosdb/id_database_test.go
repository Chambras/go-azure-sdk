var _ resourceids.ResourceId = DatabaseId{}

func TestNewDatabaseID(t *testing.T) {
	id := NewDatabaseID("12345678-1234-9876-4563-123456789012", "example-resource-group", "accountValue", "databaseRidValue")

	if id.SubscriptionId != "12345678-1234-9876-4563-123456789012" {
		t.Fatalf("Expected %q but got %q for Segment 'SubscriptionId'", id.SubscriptionId, "12345678-1234-9876-4563-123456789012")
	}

	if id.ResourceGroupName != "example-resource-group" {
		t.Fatalf("Expected %q but got %q for Segment 'ResourceGroupName'", id.ResourceGroupName, "example-resource-group")
	}

	if id.AccountName != "accountValue" {
		t.Fatalf("Expected %q but got %q for Segment 'AccountName'", id.AccountName, "accountValue")
	}

	if id.DatabaseRid != "databaseRidValue" {
		t.Fatalf("Expected %q but got %q for Segment 'DatabaseRid'", id.DatabaseRid, "databaseRidValue")
	}
}

func TestFormatDatabaseID(t *testing.T) {
	actual := NewDatabaseID("12345678-1234-9876-4563-123456789012", "example-resource-group", "accountValue", "databaseRidValue").ID()
	expected := "/subscriptions/12345678-1234-9876-4563-123456789012/resourceGroups/example-resource-group/providers/Microsoft.DocumentDB/databaseAccounts/accountValue/databases/databaseRidValue"
	if actual != expected {
		t.Fatalf("Expected the Formatted ID to be %q but got %q", expected, actual)
	}
}

